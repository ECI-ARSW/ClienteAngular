package edu.eci.arsw.model;

/**
 *
 * @author mariale
 */
public class CodeMessage {
     /*Codigo del cliente*/ 
    private String code;
    /*Id del cliente que envia el codigo a actualizar*/
    private int id;

    /**
     * @return the code
     */
    public String getCode() {
        return code;
    }

    /**
     * @param code the code to set
     */
    public void setCode(String code) {
        this.code = code;
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }
}
